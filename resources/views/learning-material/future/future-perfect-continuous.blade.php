    <strong class="fs-3 mb-3">Future Perfect Continuous Tense</strong>
    <div class="border-top border-3 border-dark-subtle pt-3">
        Future Perfect Continuous Tense adalah tense yang berfungsi menyatakan kegiatan yang sudah dilakukan sebelum waktu tertentu di masa depan.<br>
        Rumus Future Perfect Continuous Tense:
        <ul>
            <li>
                + = &#91;Subject&#93; + &#91;will/shall&#93; + have + been + &#91;verb 1 + -ing&#93;
            </li>
            <li>
                - = &#91;Subject&#93; + &#91;will/shall&#93; + not + have + been + &#91;verb 1 + -ing&#93;
            </li>
            <li>
                ? = &#91;Will/Shall&#93; + &#91;subject&#93; + have + been + &#91;verb 1 + -ing&#93;
            </li>
        </ul>
    </div>
    <div>
        Contoh positive:
        <ul>
            <li>
                She shall have been cleaning her room for the next hour.
            </li>
            <li>
                They will have been developing a new game by next spring.
            </li>
        </ul>
    </div>
    <div>
        Contoh negative:
        <ul>
            <li>
                She shall not have been cleaning her room for the next hour.
            </li>
            <li>
                They will not have been developing a new game by next spring.
            </li>
        </ul>
    </div>
    <div>
        Contoh interrogative:
        <ul>
            <li>
                Shall she have been cleaning her room for the next hour?
            </li>
            <li>
                Will they have been developing a new game by next spring?
            </li>
        </ul>
    </div>
