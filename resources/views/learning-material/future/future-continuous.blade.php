    <strong class="fs-3 mb-3">Future Continuous Tense</strong>
    <div class="border-top border-3 border-dark-subtle pt-3">
        Future Continuous Tense adalah bentuk verb yang digunakan untuk waktu yang akan datang.<br>
        Rumus Future Continuous Tense:
        <ul>
            <li>
                + Verbal = &#91;Subject&#93; + &#91;will/shall&#93; + be + &#91;verb 1 + -ing&#93;
            </li>
            <li>
                + Nominal = &#91;Noun/Noun Phrase&#93; + &#91;will/shall&#93; + be + to + be + &#91;verb 1 + -ing&#93;
            </li>
            <li>
                - Verbal = &#91;Subject&#93; + &#91;will/shall&#93; + not + be + &#91;verb 1 + -ing&#93;
            </li>
            <li>
                - Nominal = &#91;Noun/Noun Phrase&#93; + &#91;will/shall&#93; + not + be + to + be + &#91;verb 1 + -ing&#93;
            </li>
            <li>
                ? Verbal = &#91;Will/Shall&#93; + &#91;subject&#93; + &#91;verb 1 + -ing&#93;
            </li>
            <li>
                ? Nominal = &#91;Will/Shall&#93; + &#91;noun/noun phrase&#93; + be + to + be + &#91;verb 1 + -ing&#93;
            </li>
        </ul>
    </div>
    <div>
        Contoh positive:
        <ul>
            <li>
                She will be leaving for Poland tonight.
            </li>
            <li>
                Their plan will be to be swimming the following Saturday.
            </li>
        </ul>
    </div>
    <div>
        Contoh negative:
        <ul>
            <li>
                She will not be leaving for Poland tonight.
            </li>
            <li>
                Their plan will not be to be swimming the following Saturday.
            </li>
        </ul>
    </div>
    <div>
        Contoh interrogative:
        <ul>
            <li>
                Will she be leaving for Poland tonight?
            </li>
            <li>
                Will their plan be to be swimming the following Saturday?
            </li>
        </ul>
    </div>
