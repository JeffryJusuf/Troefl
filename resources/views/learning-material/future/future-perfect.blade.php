    <strong class="fs-3 mb-3">Future Perfect Tense</strong>
    <div class="border-top border-3 border-dark-subtle pt-3">
        Future Perfect Tense adalah kata kerja yang digunakan untuk keadaan yang akan berlangsung untuk jangka waktu tertentu namun pasti di masa depan.<br>
        Rumus Future Perfect Tense:
        <ul>
            <li>
                + = &#91;Subject&#93; + &#91;will/shall&#93; + have + &#91;verb 3&#93;
            </li>
            <li>
                - = &#91;Subject&#93; + &#91;will/shall&#93; + not + have + &#91;verb 3&#93;
            </li>
            <li>
                ? = &#91;Will/Shall&#93; + &#91;subject&#93; + have + &#91;verb 3&#93;
            </li>
        </ul>
    </div>
    <div>
        Contoh positive:
        <ul>
            <li>
                He will have done his assignment by the end of this month.
            </li>
            <li>
                We shall have booked that room by the end of next week.
            </li>
        </ul>
    </div>
    <div>
        Contoh negative:
        <ul>
            <li>
                He will not have done his assignment by the end of this month.
            </li>
            <li>
                We shall not have booked that room by the end of next week.
            </li>
        </ul>
    </div>
    <div>
        Contoh interrogative:
        <ul>
            <li>
                Will he have done his assignment by the end of this month?
            </li>
            <li>
                Shall we have booked that room by the end of next week?
            </li>
        </ul>
    </div>
