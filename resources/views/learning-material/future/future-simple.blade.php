    <strong class="fs-3 mb-3">Future Simple Tense</strong>
    <div class="border-top border-3 border-dark-subtle pt-3">
        Future Simple Tense adalah bentuk verb yang digunakan untuk waktu yang akan datang.<br>
        Rumus Future Simple Tense:
        <ul>
            <li>
                + Verbal = &#91;Subject&#93; + &#91;will/shall&#93; + &#91;verb 1&#93;
            </li>
            <li>
                + Verbal = &#91;Subject&#93; + &#91;is/am/are&#93; + going to + &#91;verb 1&#93;
            </li>
            <li>
                + Nominal = &#91;Noun/Noun Phrase&#93; + &#91;is/are/will be/shall be&#93; + to + &#91;verb 1&#93;
            </li>
            <li>
                - Verbal = &#91;Subject&#93; + &#91;will/shall&#93; + not + &#91;verb 1&#93;
            </li>
            <li>
                - Verbal = &#91;Subject&#93; + &#91;is/am/are&#93; + not + going to + &#91;verb 1&#93;
            </li>
            <li>
                - Nominal = &#91;Noun/Noun Phrase&#93; + &#91;is/are/will/shall&#93; + not + &#91;be &#40;kalau pakai will/shall&#41;&#93; + to + &#91;verb 1&#93;
            </li>
            <li>
                ? Verbal = &#91;Will/Shall&#93; + &#91;subject&#93; + &#91;verb 1&#93;
            </li>
            <li>
                ? Verbal = &#91;Is/Am/Are&#93; + &#91;subject&#93; + going to + &#91;verb 1&#93;
            </li>
            <li>
                ? Nominal = &#91;Will/Shall/Is/Are&#93; + &#91;noun/noun phrase&#93; + &#91;be &#40;kalau pakai will/shall&#41;&#93; + to + &#91;verb 1&#93;
            </li>
        </ul>
    </div>
    <div>
        Contoh positive:
        <ul>
            <li>
                The company will launch a new product next year.
            </li>
            <li>
                I am going to ride a motorcycle next week.
            </li>
            <li>
                His intention is to buy a new car next month.
            </li>
            <li>
                Our aim will be to go on vacation next summer.
            </li>
        </ul>
    </div>
    <div>
        Contoh negative:
        <ul>
            <li>
                The company will not launch a new product next year.
            </li>
            <li>
                I am not going to ride a motorcycle next week.
            </li>
            <li>
                His intention is not to buy a new car next month.
            </li>
            <li>
                Our aim will not be to go on vacation next summer.
            </li>
        </ul>
    </div>
    <div>
        Contoh interrogative:
        <ul>
            <li>
                Will the company launch a new product next year?
            </li>
            <li>
                Am I going to ride a motorcycle next week?
            </li>
            <li>
                Is his intention to buy a new car next month?
            </li>
            <li>
                Will our aim be to go on vacation next summer?
            </li>
        </ul>
    </div>
