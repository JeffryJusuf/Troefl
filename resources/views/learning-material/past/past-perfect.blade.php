    <strong class="fs-3 mb-3">Past Perfect Tense</strong>
    <div class="border-top border-3 border-dark-subtle pt-3">
        Past Perfect Tense adalah untuk menunjukan suatu pekerjaan di masa lalu sudah selesai dilakukan sebelum pekerjaan lain dimulai di masa lalu.<br>
        Rumus Past Perfect Tense:
        <ul>
            <li>
                + = &#91;Subject&#93; + had + &#91;verb 3&#93;
            </li>
            <li>
                - = &#91;Subject&#93; + had + not + &#91;verb 3&#93;
            </li>
            <li>
                ? = Had + &#91;subject&#93; + &#91;verb 3&#93;
            </li>
        </ul>
    </div>
    <div>
        Contoh positive:
        <ul>
            <li>
                He has called this afternoon.
            </li>
            <li>
                I had made changes to the requirement last night.
            </li>
        </ul>
    </div>
    <div>
        Contoh negative:
        <ul>
            <li>
                He has not called this afternoon.
            </li>
            <li>
                I had not made changes to the requirement last night.
            </li>
        </ul>
    </div>
    <div>
        Contoh interrogative:
        <ul>
            <li>
                Had he called this afternoon?
            </li>
            <li>
                Had I made changes to the requirement last night?
            </li>
        </ul>
    </div>
