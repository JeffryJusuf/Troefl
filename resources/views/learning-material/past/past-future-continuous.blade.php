    <strong class="fs-3 mb-3">Past Future Continuous Tense</strong>
    <div class="border-top border-3 border-dark-subtle pt-3">
        Past Future Continuous Tense adalah verb yang mengungkapkan rencana yang akan atau sedang terjadi dan berpindah dari future ke past.<br>
        Rumus Past Future Continuous Tense:
        <ul>
            <li>
                + = &#91;Subject&#93; + &#91;would/should&#93; + be + &#91;verb 1 + -ing&#93;
            </li>
            <li>
                - = &#91;Subject&#93; + &#91;would/should&#93; + not + be + &#91;verb 1 + -ing&#93;
            </li>
            <li>
                ? = &#91;Would/Should&#93; + &#91;subject&#93; + be + &#91;verb 1 + -ing&#93;
            </li>
        </ul>
    </div>
    <div>
        Contoh positive:
        <ul>
            <li>
                She would be moving about a week ago.
            </li>
            <li>
                They should be performing three days ago.
            </li>
        </ul>
    </div>
    <div>
        Contoh negative:
        <ul>
            <li>
                She would not be moving about a week ago.
            </li>
            <li>
                They should not be performing three days ago.
            </li>
        </ul>
    </div>
    <div>
        Contoh interrogative:
        <ul>
            <li>
                Should she be moving about a week ago?
            </li>
            <li>
                Would they be performing three days ago?
            </li>
        </ul>
    </div>
