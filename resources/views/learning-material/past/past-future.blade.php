    <strong class="fs-3 mb-3">Past Future Tense</strong>
    <div class="border-top border-3 border-dark-subtle pt-3">
        Past Future Tense adalah suatu kejadian yang direncanakan atau dijanjikan di masa yang akan dating.<br>
        Rumus Past Future Tense:
        <ul>
            <li>
                + = &#91;Subject&#93; + &#91;would/should&#93; + &#91;verb 1&#93;
            </li>
            <li>
                - = &#91;Subject&#93; + &#91;would/should&#93; + not + &#91;verb 1&#93;
            </li>
            <li>
                ? = &#91;Would/Should&#93; + &#91;subject&#93; + &#91;verb 1&#93;
            </li>
        </ul>
    </div>
    <div>
        Contoh positive:
        <ul>
            <li>
                He should finish the report by Friday.
            </li>
            <li>
                We would play football this Sunday.
            </li>
        </ul>
    </div>
    <div>
        Contoh negative:
        <ul>
            <li>
                He should not finish the report by Friday.
            </li>
            <li>
                We would not play football this Sunday.
            </li>
        </ul>
    </div>
    <div>
        Contoh interrogative:
        <ul>
            <li>
                Should he finish the report by Friday?
            </li>
            <li>
                Would we play football this Sunday?
            </li>
        </ul>
    </div>
