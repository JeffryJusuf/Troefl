    <strong class="fs-3 mb-3">Past Future Perfect Continuous Tense</strong>
    <div class="border-top border-3 border-dark-subtle pt-3">
        Past Future Perfect Continuous Tense adalah untuk menyatakan suatu kegiatan yang mempunyai batas waktu tertentu di masa lampau dan akan diperkirakan selesai dalam jangka waktu tertentu tersebut.<br>
        Rumus Past Future Perfect Continuous Tense:
        <ul>
            <li>
                + = &#91;Subject&#93; + &#91;would/should&#93; + have + been + &#91;verb 1 + -ing&#93;
            </li>
            <li>
                - = &#91;Subject&#93; + &#91;would/should&#93; + not + have + been + &#91;verb 1 + -ing&#93;
            </li>
            <li>
                ? = &#91;Would/Should&#93; + &#91;subject&#93; + have + been + &#91;verb 1 + -ing&#93;
            </li>
        </ul>
    </div>
    <div>
        Contoh positive:
        <ul>
            <li>
                She would have been waiting for your approval by the end of this week.
            </li>
            <li>
                they should have been traveling around the world by this time next year.
            </li>
        </ul>
    </div>
    <div>
        Contoh negative:
        <ul>
            <li>
                She would not have been waiting for your approval by the end of this week.
            </li>
            <li>
                they should not have been traveling around the world by this time next year.
            </li>
        </ul>
    </div>
    <div>
        Contoh interrogative:
        <ul>
            <li>
                Would she have been waiting for your approval by the end of this week?
            </li>
            <li>
                Should they have been traveling around the world by this time next year?
            </li>
        </ul>
    </div>
