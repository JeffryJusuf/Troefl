    <strong class="fs-3 mb-3">Past Future Perfect Tense</strong>
    <div class="border-top border-3 border-dark-subtle pt-3">
        Past Future Perfect Tense adalah kata kerja yang menyatakan aksi di masa lalu yang sudah direncanakan atau sudah dilaksanakan sebelum waktu tertentu di masa depan dari sudut pandang masa lampau.<br>
        Rumus Past Future Perfect Tense:
        <ul>
            <li>
                + = &#91;Subject&#93; + &#91;would/should&#93; + &#91;have&#93; + &#91;verb 3&#93;
            </li>
            <li>
                - = &#91;Subject&#93; + &#91;would/should&#93; + not + &#91;have&#93; + &#91;verb 3&#93;
            </li>
            <li>
                ? = &#91;Would/Should&#93; + &#91;subject&#93; + &#91;have&#93; + &#91;verb 3&#93;
            </li>
        </ul>
    </div>
    <div>
        Contoh positive:
        <ul>
            <li>
                He should have arrived yesterday.
            </li>
            <li>
                I would have eaten last night.
            </li>
        </ul>
    </div>
    <div>
        Contoh negative:
        <ul>
            <li>
                He should not have arrived yesterday.
            </li>
            <li>
                I would not have eaten last night.
            </li>
        </ul>
    </div>
    <div>
        Contoh interrogative:
        <ul>
            <li>
                Should he have arrived yesterday?
            </li>
            <li>
                Would I have eaten last night?
            </li>
        </ul>
    </div>
