    <strong class="fs-3 mb-3">Past Perfect Continuous Tense</strong>
    <div class="border-top border-3 border-dark-subtle pt-3">
        Past Perfect Continuous Tense adalah bentuk verb yang digunakan untuk kejadian yang sedang berlangsung sebelum beberapa kegiatan lainnya terjadi.<br>
        Rumus Past Perfect Continuous Tense:
        <ul>
            <li>
                + = &#91;Subject&#93; + had + been + &#91;verb 1 + -ing&#93;
            </li>
            <li>
                - = &#91;Subject&#93; + had + not + been + &#91;verb 1 + -ing&#93;
            </li>
            <li>
                ? = Had + &#91;subject&#93; + been + &#91;verb 1 + -ing&#93;
            </li>
        </ul>
    </div>
    <div>
        Contoh positive:
        <ul>
            <li>
                She had been studying for two hours.
            </li>
            <li>
                We had been walking for the past 10 minutes.
            </li>
        </ul>
    </div>
    <div>
        Contoh negative:
        <ul>
            <li>
                She had not been studying for two hours.
            </li>
            <li>
                We had not been walking for the past 10 minutes.
            </li>
        </ul>
    </div>
    <div>
        Contoh interrogative:
        <ul>
            <li>
                Had she been studying for two hours?
            </li>
            <li>
                Had we been walking for the past 10 minutes?
            </li>
        </ul>
    </div>
