    <strong class="fs-3 mb-3">Past Simple Tense</strong>
    <div class="border-top border-3 border-dark-subtle pt-3">
        Past Simple Tense digunakan untuk mengekspresikan kejadian di masa lampau yang sudah selesai.<br>
        Rumus Past Simple Tense:
        <ul>
            <li>
                + = &#91;Subject&#93; + &#91;verb 2&#93;
            </li>
            <li>
                - = &#91;Subject&#93; + did + not + &#91;verb 1&#93;
            </li>
            <li>
                ? = Did + &#91;subject&#93; + &#91;verb 1&#93;
            </li>
        </ul>
    </div>
    <div>
        Contoh positive:
        <ul>
            <li>
                He mopped the floor recently.
            </li>
            <li>
                We went fishing a few days ago.
            </li>
        </ul>
    </div>
    <div>
        Contoh negative:
        <ul>
            <li>
                He did not mop the floor this morning.
            </li>
            <li>
                We did not go fishing a few days ago.
            </li>
        </ul>
    </div>
    <div>
        Contoh interrogative:
        <ul>
            <li>
                Did he mop the floor this morning?
            </li>
            <li>
                Did we go fishing a few days ago?
            </li>
        </ul>
    </div>
