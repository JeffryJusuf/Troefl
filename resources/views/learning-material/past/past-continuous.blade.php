    <strong class="fs-3 mb-3">Past Continuous Tense</strong>
    <div class="border-top border-3 border-dark-subtle pt-3">
        Past Continuous Tense digunakan untuk mengekspresikan kejadian di masa lampau yang sudah selesai.<br>
        Rumus Past Continuous Tense:
        <ul>
            <li>
                + = &#91;Subject&#93; + &#91;was/were&#93; + &#91;verb 1 + -ing&#93;
            </li>
            <li>
                - = &#91;Subject&#93; + &#91;was/were&#93; + not + &#91;verb 1 + -ing&#93;
            </li>
            <li>
                ? = &#91;Was/Were&#93; + &#91;subject&#93; + &#91;verb 1 + -ing&#93;
            </li>
        </ul>
    </div>
    <div>
        Contoh positive:
        <ul>
            <li>
                She was writing a letter yesterday.
            </li>
            <li>
                They were filming a movie a few weeks ago.
            </li>
        </ul>
    </div>
    <div>
        Contoh negative:
        <ul>
            <li>
                She was not writing a letter yesterday.
            </li>
            <li>
                They were not filming a movie a few weeks ago.
            </li>
        </ul>
    </div>
    <div>
        Contoh interrogative:
        <ul>
            <li>
                Was she writing a letter yesterday?
            </li>
            <li>
                Were they filming a movie a few weeks ago?
            </li>
        </ul>
    </div>
