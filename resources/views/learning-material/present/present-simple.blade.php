    <strong class="fs-3 mb-3">Present Simple Tense</strong>
    <div class="border-top border-3 border-dark-subtle pt-3">
        Present Simple Tense digunakkan untuk mengekspresikan tindakan yang berulang atau tidak berubah. Biasanya berupa  hobi, fakta, keinginan, dan kebiasaan.<br>
        Rumus Present Simple Tense:
        <ul>
            <li>
                + Verbal = &#91;Subject&#93; + &#91;verb 1 &#40;+ -s/-es kalau subject itu singular&#41;&#93;
            </li>
            <li>
                + Nominal = &#91;Subject&#93; + &#91;is/am/are&#93; + &#91;nominal&#93;
            </li>
            <li>
                - Verbal = &#91;Subject&#93; + &#91;does/do&#93; + not + &#91;verb 1&#93;
            </li>
            <li>
                - Nominal = &#91;Subject&#93; + &#91;is/am/are&#93; + not + &#91;verb 1&#93;
            </li>
            <li>
                ? Verbal = &#91;Does/Do&#93; + &#91;subject&#93; + &#91;verb 1&#93;
            </li>
            <li>
                ? Nominal = &#91;Is/Am/Are&#93; + &#91;subject&#93; + &#91;verb 1&#93;
            </li>
        </ul>
    </div>
    <div>
        Contoh positive:
        <ul>
            <li>
                He works as an office worker.
            </li>
            <li>
                He is an office worker.
            </li>
        </ul>
    </div>
    <div>
        Contoh negative:
        <ul>
            <li>
                He does not work as an office worker.
            </li>
            <li>
                He is not an office worker.
            </li>
        </ul>
    </div>
    <div>
        Contoh interrogative:
        <ul>
            <li>
                Does he work as an office worker?
            </li>
            <li>
                Is he an office worker?
            </li>
        </ul>
    </div>
