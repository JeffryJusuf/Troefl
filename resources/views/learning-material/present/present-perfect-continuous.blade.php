    <strong class="fs-3 mb-3">Present Perfect Continuous Tense</strong>
    <div class="border-top border-3 border-dark-subtle pt-3">
        Present Perfect Continuous Tense ini berfungsi untuk menjelaskan sebuah situasi yang sedang berlangsung sebelum terjadinya suatu kejadian lainnya.<br>
        Rumus Present Perfect Continuous Tense:
        <ul>
            <li>
                + = &#91;Subject&#93; + &#91;has/have&#93; + been + &#91;verb 1 + -ing&#93;
            </li>
            <li>
                - = &#91;Subject&#93; + &#91;has/have&#93; + not + been + &#91;verb 1 + -ing&#93;
            </li>
            <li>
                ? = &#91;Has/Have&#93; + &#91;subject&#93; + been + &#91;verb 1 + -ing&#93;
            </li>
        </ul>
    </div>
    <div>
        Contoh positive:
        <ul>
            <li>
                She has been cooking for two hours.
            </li>
            <li>
                I have been switching between channels for a while.
            </li>
        </ul>
    </div>
    <div>
        Contoh negative:
        <ul>
            <li>
                She has not been cooking at the moment.
            </li>
            <li>
                I have not been switching between channels.
            </li>
        </ul>
    </div>
    <div>
        Contoh interrogative:
        <ul>
            <li>
                Has she been cooking at the moment?
            </li>
            <li>
                Have I been switching between channels?
            </li>
        </ul>
    </div>
