    <strong class="fs-3 mb-3">Present Continuous Tense</strong>
    <div class="border-top border-3 border-dark-subtle pt-3">
        Present Continuous Tense memiliki fungsi untuk mengekspresikan tindakan yang berkelanjutan dan bentuk ini juga dapat menunjukkan suatu keadaan yang sedang terjadi atau akan segera terjadi.<br>
        Rumus Present Continuous Tense:
        <ul>
            <li>
                + = &#91;Subject&#93; + &#91;is/am/are&#93; + &#91;verb 1 + -ing&#93;
            </li>
            <li>
                - = &#91;Subject&#93; + &#91;is/am/are&#93; + not + &#91;verb 1 + -ing&#93;
            </li>
            <li>
                ? = &#91;Is/Am/Are&#93; + &#91;subject&#93; + &#91;verb 1 + -ing&#93;
            </li>
        </ul>
    </div>
    <div>
        Contoh positive:
        <ul>
            <li>
                He is visiting his grandparents at the moment.
            </li>
            <li>
                They are not watching movie right now.
            </li>
        </ul>
    </div>
    <div>
        Contoh negative:
        <ul>
            <li>
                He is not visiting his grandparents at the moment.
            </li>
            <li>
                They are not not watching movie right now.
            </li>
        </ul>
    </div>
    <div>
        Contoh interrogative:
        <ul>
            <li>
                Is he visiting his grandparents at the moment?
            </li>
            <li>
                Are they watching movie right now?
            </li>
        </ul>
    </div>
