    <strong class="fs-3 mb-3">Present Perfect Tense</strong>
    <div class="border-top border-3 border-dark-subtle pt-3">
        Present Perfect Tense biasa digunakan untuk mengekspresikan kejadian yang terjadi di masa lalu dan masih berlanjut hingga sekarang atau saat ini.<br>
        Rumus Present Perfect Tense:
        <ul>
            <li>
                + = &#91;Subject&#93; + &#91;has/have&#93; + &#91;verb 3&#93;
            </li>
            <li>
                - = &#91;Subject&#93; + &#91;has/have&#93; + not + &#91;verb 3&#93;
            </li>
            <li>
                ? = &#91;Has/Have&#93; + &#91;subject&#93; + &#91;verb 3&#93;
            </li>
        </ul>
    </div>
    <div>
        Contoh positive:
        <ul>
            <li>
                She has visited Paris several times.
            </li>
            <li>
                I have read that book already.
            </li>
        </ul>
    </div>
    <div>
        Contoh negative:
        <ul>
            <li>
                She has not visited Paris even once.
            </li>
            <li>
                I have not read that book before.
            </li>
        </ul>
    </div>
    <div>
        Contoh interrogative:
        <ul>
            <li>
                Has she visited Paris even once?
            </li>
            <li>
                Have I read that book before?
            </li>
        </ul>
    </div>
